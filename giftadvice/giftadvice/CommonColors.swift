import UIKit

// MARK: Common Colors

extension AppColors {
    struct Common {
        static func shadow() -> UIColor {
            return UIColor(red: 0.0 / 255.0, green: 0.0 / 255.0, blue: 0.0 / 255.0, alpha: 0.12)
        }
        
        static func active() -> UIColor {
            return UIColor(red: 0.0 / 255.0, green: 166.0 / 255.0, blue: 255.0 / 255.0, alpha: 1.0)
        }
    }
}
